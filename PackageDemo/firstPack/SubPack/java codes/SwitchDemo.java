class SwitchDemo{
public static void main (String[] args){
  char choice='a';
		 switch(choice){
		 	case 'a':
		 		System.out.println("Inside a Block");
		 		break;
		 	case 'b' :
		 		System.out.println("b");
		 		break;
		 	case 'c':
		 		System.out.println("c");
		 		break;
		  	}

	}

}